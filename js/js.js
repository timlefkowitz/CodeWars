/*
 Convert boolean values to strings 'Yes' or 'No'.
 */
function boolToWord( bool ){
    //...
    if(bool === true){
        return "Yes";
    } else {
        return "No";
    }
}

/*
Convert a Number to a String!
 */
function numberToString(num) {
    // Return a string of the number here!
    return num.toString();
}

/*
Multiply
 */
function multiply(a, b){
    return a * b;
}

